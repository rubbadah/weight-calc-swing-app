package project04;

// ユーザクラス
public class User {
	/**
	 * 名前
	 */
	String name;
	/**
	 * 身長
	 */
	double height;
	/**
	 * 体重
	 */
	double weight;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}
}
